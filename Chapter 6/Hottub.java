public class Hottub {

    public Hottub(){
    }

    public void bubble(){
        System.out.println("Hottub is bubbling!");
    }

    public void off(){
        System.out.println("Hottub is off!");
    }

    public void setTemperature(int temp){
        System.out.println("Temperature in hottub is set to " + temp);
    }
}
